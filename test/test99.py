def search_products(keyword):
    start_time = time.time()

    # 将搜索关键词拆分成单个字符
    chars = list(set(keyword))  # 使用set去重

    # 创建结果字典，用于存储商品和匹配度
    results = defaultdict(int)

    # 遍历所有商品
    for product in products:
        # 计算每个商品包含关键字字符的数量
        match_count = sum(1 for char in chars if char in product)
        if match_count > 0:  # 只记录有匹配的商品
            results[product] = match_count

    # 按匹配度排序
    sorted_results = sorted(results.items(), key=lambda x: (-x[1], x[0]))

    end_time = time.time()

    # 打印结果
    print(f"\n搜索关键词: {keyword}")
    print(f"搜索用时: {(end_time - start_time) * 1000:.2f}ms")
    print("\n排序结果（前10条）:")
    for product, score in sorted_results[:10]:
        print(f"商品: {product:<15} 匹配度: {score}")


